void Get_m0_m12(TFile *file, TH2D* histo, TH1D* h1, int nbinCut, int nbinRef, int efficiency){

  TH1I* runNumber  = (TH1I*)(file ->Get("runNumber_histo")) ;  
  double run = (runNumber ->GetMean());

  double m0=0.;
  double m12=0.;
  
  get_m0_m12(run, &m0, &m12);

  double eff = 0.;
  Double_t nbin_content_ref = h1->GetBinContent(nbinRef);
  Double_t nbin_content_cut = h1->GetBinContent(nbinCut);


  if (efficiency){
    if (nbin_content_ref) eff = nbin_content_cut/nbin_content_ref; 
    histo->Fill(m0, m12, eff);
    //cout << run<< " eff: "<<eff << endl; 
  }else{ 
    cout << run<< " evts: "<<nbin_content_cut << endl; 
    histo->Fill(m0, m12, nbin_content_cut);
  }

  return;

}

